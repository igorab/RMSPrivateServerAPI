namespace RMSPrivateServerAPI.Enums;

/// <summary>
/// Перечисление возможных состояний конечного автомата (FSM) робота.
/// Каждое состояние соответствует определённому этапу работы или режиму управления.
/// </summary>
public enum RobotFsmState : byte
{
    /// <summary>
    /// Робот в режиме простоя (не готов к работе).
    /// </summary>
    Idle = 0,

    /// <summary>
    /// Инициализация оборудования (калибровка, проверка датчиков).
    /// </summary>
    Initializing = 1,

    /// <summary>
    /// Робот готов к выполнению задач (ожидает команды).
    /// </summary>
    Ready = 2,

    /// <summary>
    /// Режим ручного управления (например, с пульта).
    /// </summary>
    ManualControl = 3,

    /// <summary>
    /// Автоматическое движение к заданной позиции (с объездом препятствий).
    /// </summary>
    Navigation = 4,

    /// <summary>
    /// Прямолинейное движение к точке (без коррекции угла).
    /// </summary>
    LinearMotion = 5,

    /// <summary>
    /// Поворот на месте до заданного угла (heading).
    /// </summary>
    Rotation = 6,

    /// <summary>
    /// Движение по дуге окружности (с изменением угла).
    /// </summary>
    ArcMotion = 7,

    /// <summary>
    /// Точное выравнивание для захвата паллеты (подъезд к стеллажу).
    /// </summary>
    Adjustment = 8,

    /// <summary>
    /// Погрузка паллеты (вилы затягивают паллету на робот).
    /// </summary>
    Loading = 9,

    /// <summary>
    /// Выгрузка паллеты (вилы снимают паллету с робота).
    /// </summary>
    Unloading = 10,

    /// <summary>
    /// Погрузка паллеты на месте (робот подъезжает под паллету).
    /// </summary>
    LoadingInPlace = 11,

    /// <summary>
    /// Выгрузка паллеты на месте (робот отъезжает из-под паллеты).
    /// </summary>
    UnloadingInPlace = 12,

    /// <summary>
    /// Робот находится на парковочной позиции.
    /// </summary>
    Parking = 13,

    /// <summary>
    /// Робот подключён к зарядной станции и заряжается.
    /// </summary>
    Charging = 14,

    /// <summary>
    /// Зарядка завершена, но робот ещё на станции.
    /// </summary>
    ChargeFinished = 15,

    /// <summary>
    /// Ожидание синхронизации (например, подтверждения от сервера).
    /// </summary>
    WaitSync = 16,

    /// <summary>
    /// Приостановка выполнения текущей подзадачи (временная остановка).
    /// </summary>
    Paused = 17,

    /// <summary>
    /// Аварийная ситуация (требуется вмешательство оператора).
    /// </summary>
    Alarm = 18
}

